export function useAccountFindMany<
  T extends Prisma.accountFindManyArgs,
  R extends GetFindResult<Prisma.$accountPayload<DefaultArgs>, T>[],
>(
  args?: Prisma.SelectSubset<T, Prisma.accountFindManyArgs>,
  options?: RequestOptions,
  swrOptions?: SWRRequestOptions<R>,
) {
  const roq = useRoqClient();
  const key = JSON.stringify(['useAccountFindMany', args || {}]);
  return useSWR<R, Error>(
    key,
    async () => {
      const result = await roq.account.findMany(args, options);
      return result as R;
    },
    {
      ...swrOptions,
      fallbackData: swrOptions?.initialData ?? swrOptions?.fallbackData,
    },
  );
}

